#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED
/** Aca van los prototipos de las funciones*/

/** El primer "float" indica el tipo de dato que devuelve la funcion, "sumar" es el
nombre de la funcion y los otros 2 flotantes son el tipo de dato que se le pasa a la funcion*/
float sumar(float,float);

/** El primer "float" indica el tipo de dato que devuelve la funcion, "resta" es el
nombre de la funcion y los otros 2 flotantes son el tipo de dato que se le pasa a la funcion*/
float resta (float,float);

/** El primer "float" indica el tipo de dato que devuelve la funcion, "dividir" es el
nombre de la funcion y los otros 2 flotantes son el tipo de dato que se le pasa a la funcion*/
float dividir(float,float);

/** El primer "float" indica el tipo de dato que devuelve la funcion, "multiplicar" es el
nombre de la funcion y los otros 2 flotantes son el tipo de dato que se le pasa a la funcion*/
float multiplicar(float,float);

/** "int" es el tipo de dato que devuelve la funcion (entero), "factorial" es el nombre de la funcion e "int" el tipo de dato
que devuelve*/
int factorial(int);


#endif // FUNCIONES_H_INCLUDED
